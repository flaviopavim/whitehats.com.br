Política de Privacidade do WhiteHatss
<br>
Última atualização: 21/10/2023
<br>
<br>

O WhiteHats compreende a importância da privacidade e está comprometido em proteger 
os dados dos usuários. Esta Política de Privacidade tem o propósito de informar 
que não coletamos dados pessoais em nossos sites e aplicativos.
<br>
<br>

Nós não coletamos, armazenamos ou processamos informações pessoais dos usuários em nossos sites e aplicativos.
<br>
<br>

Nossos sites e aplicativos não utilizam cookies ou tecnologias semelhantes para rastrear ou coletar informações dos usuários. Não armazenamos dados de navegação, preferências do usuário ou histórico de acesso.
<br>
<br>

Finalidades
<br>
Como não coletamos dados pessoais, não utilizamos informações para nenhum fim específico, como fornecer serviços, personalizar a experiência do usuário ou para fins de marketing.
